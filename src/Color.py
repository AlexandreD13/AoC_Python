class Color:
    """
    Exit codes available for print customization

    """

    BLUE: str = "\033[94m"
    CYAN: str = "\033[96m"
    GREEN: str = "\033[92m"
    PURPLE: str = "\033[95m"
    RED: str = "\033[91m"
    YELLOW: str = "\033[93m"

    BOLD: str = "\033[1m"
    END: str = "\033[0m"
    UNDERLINED: str = "\033[4m"
